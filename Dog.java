public class Dog
{
  private String breed;
  private double weight;
  private String name;
  private String color;

  // Make setters
  public void setBreed(String iBreed)
  {
    breed = iBreed;
  }

  public void setWeight ( double iWeight)
  {
    weight = iWeight;
  }

  // Make getters
  public String getBreed()
  {
    return breed;
  }

  public String getWeight()
  {
    return weight;
  }
}
